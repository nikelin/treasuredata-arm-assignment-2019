package co.jp.treasuredata.armtd.server.server.commands.discovery;

public class DiscovererException extends Throwable {

    public DiscovererException() {
        super();
    }

    public DiscovererException(String message) {
        super(message);
    }

    public DiscovererException(String message, Throwable cause) {
        super(message, cause);
    }

    public DiscovererException(Throwable cause) {
        super(cause);
    }

    public DiscovererException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
